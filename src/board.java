public class board {

    public void generate(int n){
        for(int i = 0; i< n;i++){
            for(int j = 0; j < n; j++){
                for(int z = 0; z < n; z++){
                    System.out.println(i + " ");
                    System.out.println(j + " ");
                    System.out.println(z + " ");
                }
            }
        }
    }
}
